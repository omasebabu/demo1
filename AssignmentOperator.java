public class AssignmentOperator {

	public static void main(String[] args) {
		int number=10;//assignment operator (=):will assign 10 values to number variable
		System.out.println("Number is:"+number);
		number+=20;
		System.out.println("After Adding 20 Number is:"+number);
		number-=10;
		System.out.println("After minus 20 number is:"+number);
		number*=5;
		System.out.println("After multiply 5 number is:"+number);
		number/=12;
		System.out.println("After divide 12 number is:"+number);
	}

}
